package model;

import java.sql.Date;
import java.sql.Time;

public class AdminViewOrderDetails {
    private String orderId;
    private Date orderDate;
    private int userId;
    private String address;
    private String paymentMethod;
    private String shippedStatus;
    private String trackingNo;
    private Date shipDate;
    private Time shipTime;
    private String arrivalStatus;

    
    // Getters and Setters
    public String getOrderId() {
        return orderId;
    }

    public void setOrderId(String orderId) {
        this.orderId = orderId;
    }

    public Date getOrderDate() {
        return orderDate;
    }

    public void setOrderDate(Date orderDate) {
        this.orderDate = orderDate;
    }

    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getPaymentMethod() {
        return paymentMethod;
    }

    public void setPaymentMethod(String paymentMethod) {
        this.paymentMethod = paymentMethod;
    }

    public String getShippedStatus() {
        return shippedStatus;
    }

    public void setShippedStatus(String shippedStatus) {
        this.shippedStatus = shippedStatus;
    }

    public String getTrackingNo() {
        return trackingNo;
    }

    public void setTrackingNo(String trackingNo) {
        this.trackingNo = trackingNo;
    }

    public Date getShipDate() {
        return shipDate;
    }

    public void setShipDate(Date shipDate) {
        this.shipDate = shipDate;
    }

    public Time getShipTime() {
        return shipTime;
    }

    public void setShipTime(Time shipTime) {
        this.shipTime = shipTime;
    }

    public String getArrivalStatus() {
        return arrivalStatus;
    }

    public void setArrivalStatus(String arrivalStatus) {
        this.arrivalStatus = arrivalStatus;
    }
}
